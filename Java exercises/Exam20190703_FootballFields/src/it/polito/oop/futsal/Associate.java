package it.polito.oop.futsal;

public class Associate {
	
	private String first;
	private String last;
	private String mobile;
	private int code;

	public Associate(String first, String last, String mobile, int code) {
		this.first = first;
		this.last = last;
		this.mobile = mobile;
		this.code = code;
	}

	public String getFirst() {
		return first;
	}

	public String getLast() {
		return last;
	}

	public String getMobile() {
		return mobile;
	}

	public int getCode() {
		return code;
	}
	
	

}
